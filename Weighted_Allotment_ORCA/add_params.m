function params=add_params(xf,yf,thetaf,xi,yi,thetai)

% params - Updating parameters for waypoint update
%
% Syntax: params=add_params(xf,yf,thetaf,xi,yi,thetai)
%
    
    params.xf=xf;
    params.yf=yf;
    params.thetaf=thetaf;
    params.thetai=thetai;
    params.xi=xi;
    params.yi=yi;
    params.kf=0;
    %params.
    
end

    